
curso = "  Python "

# Tudo maiúsculo
print(curso.upper())

# Tudo minúsculo
print(curso.lower())

# Primeira maiúscula e o resto minúsculo
print(curso.title())

# Eliminando espaços em branco
print(curso.strip())

# Eliminando espaços da esquerda
print(curso.lstrip())

# Eliminando espaços da direita
print(curso.rstrip())

# Junções e centralização
curso = "Python"

print(curso.center(10, "#"))

print(".".join(curso))



